     public void llenarDatos(){
        String consulta="select * from roles";
        String tipo = "";
        try {
            Statement sentencia=ConexionBase.obtener().createStatement();
            ResultSet resultado=sentencia.executeQuery(consulta);
            while (resultado.next())
            {
                System.out.println (resultado.getObject("tipo_rol") );
                tipo = resultado.getString("tipo_rol");
                if("1".equalsIgnoreCase(tipo)){
                    jComboTipo.addItem("Administrador");
                }
                if("2".equalsIgnoreCase(tipo)){
                    jComboTipo.addItem("Empleado");
                }
            }
        } catch (Exception e) {
        }
    }

